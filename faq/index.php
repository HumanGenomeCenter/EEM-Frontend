<h2>FAQ - Frequently Asked Questions</h2>

<h3>How long do you keep the result files?</h3>
2 weeks. Please download and save your result. If you provided us with an email, we will send you a message announcing the impeding deletion of your results files. This should give you enough time to download your data.<br>
Your email will also be deleted after 2 weeks and NEVER be passed on to others.

<h3>What are the file-size limits?</h3>
64MB is the maximum size for the expression and geneset files. 


<h3>Are my results visible to the public?</h3>

Yes, although they are <em>secured by obscurity</em>, they are visible to anyone with the process ID, from with the URI of the results can be deduced. Example: https://eem.hgc.jp/&lt;process_ID&gt;<br>
We do, however, not publish a list of results. If you prefer to run EEM on your own machine, an EEM R Package is available by the same authors.


<h3>How long does the supercomputer need to finish the EEM calculations?</h3>

Depends on the size and complexity of your files. For most cases, the calculation should be finished within an hour.

<h3>How fast is your supercomputer?</h3>

The supercomputer at the Human Genome Center of the Institute of Medical Science at the University of Tokyo is described in more detail <a href="http://www.hgc.jp">here</a>.<br>

<h3>Can I see finished example results?</h3>

Please have a look at the following link: <a href="http://eem.hgc.jp/example_result/">Example Result</a>.<br>





